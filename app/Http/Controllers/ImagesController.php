    {
        $image = $request->file('image');
        $filename = time() . '.' . $image->getClientOriginalExtension();
        $imagePath = $image->storeAs('public/images', $filename);
        $image = Image::create([
            'filename' => $imagePath,
            'post_id' => $request->post_id,
        ]);
        return response()->json([
            'status' => 'success',
            'message' => 'Image added successfully',
        ]);
    }

